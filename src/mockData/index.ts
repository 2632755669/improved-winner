export const getDetailConent = () => {
  const detailList = Object.entries(detailContent).filter(
    (item: any) => detailContentKeys.includes(item[0]) && item[1].title,
  );
  const result = detailList?.map((item: any, index: number) => {
    return {
      id: `detail-${index}`,
      title: item[1].title,
      content: item[1].content,
    };
  });
  return result;
};
